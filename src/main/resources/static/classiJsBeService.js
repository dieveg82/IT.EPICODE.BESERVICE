
class Fattura {

    constructor(data, numeroFattura, anno, importo) {
        this.data = data;
        this.numeroFattura = numeroFattura;
        this.anno = anno;
        this.importo = importo;
    }
}

class StatoFattura {

    constructor(id , nome){
        this.id = id;
        this.nome = nome;
    }
}
